count = 1
while count <= 4:
    print("Looping!")
    count +=1
    
#List    
colors = ['blue', 'green', 'red', 'purple']
for color in colors:
    print(color)

#Tuple
point = (1,2,3)
for value in point:
    print(value)
    
#Dictionary
ages = {'kevin': 59, 'bob': 40, 'kayla': 21}
for key in ages:
    print(key)